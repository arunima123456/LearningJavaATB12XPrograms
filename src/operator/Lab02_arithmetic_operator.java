package operator;

public class Lab02_arithmetic_operator
{
    public static void main(String[] args) {
        int a=20;
        int b=10;
        float c=3.02f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);
    }
}
